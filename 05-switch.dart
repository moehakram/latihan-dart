void main() {
  const number = 3;

  switch (number) {
    case 0:
      print('Zero');
      break;
    case 1:
      print('one');
      break;
    case 2:
      print('two');
      break;
    case 3:
      print('three');
      break;
    case 4:
      print('four');
      break;
    default:
      print('something else');
      break;
  }
}
